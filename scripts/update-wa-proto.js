const { existsSync, mkdirSync, mkdtempSync, readFileSync, readdirSync, rmSync, writeFileSync } = require('fs');
const { join } = require('path');
const { tmpdir } = require('os');
const { execFileSync } = require('child_process');

const ROOT_DIR = join(__dirname, '..');
const SCRIPTS_DIR = join(ROOT_DIR, 'scripts');
const WA_PROTO_DIR = join(ROOT_DIR, 'WAProto');
const SOURCE_PROTO_PATH = join(WA_PROTO_DIR, 'WAProto.proto');
const BUNDLE_PATH = join(WA_PROTO_DIR, 'index.js');
const WA_VERSION_PATH = join(WA_PROTO_DIR, 'wa-version.txt');
const LOCAL_PROTO_EXTRACT_PATH = join(SCRIPTS_DIR, 'proto-extract.js');
const STUB_DTS = 'export = $root;\ndeclare var $root: any;\n';

function installExtractorDependencies() {
    const depsRoot = mkdtempSync(join(tmpdir(), 'yebail-proto-deps-'));

    execFileSync('npm', ['install', '--no-save', '--prefix', depsRoot, 'request-promise-native', 'acorn', 'acorn-walk'], {
        cwd: ROOT_DIR,
        stdio: 'inherit'
    });

    return depsRoot;
}

function generateProtoSourceLocally(nodeModulesPath) {
    if (!existsSync(LOCAL_PROTO_EXTRACT_PATH)) {
        throw new Error('Missing scripts/proto-extract.js');
    }

    execFileSync('node', [LOCAL_PROTO_EXTRACT_PATH], {
        cwd: SCRIPTS_DIR,
        stdio: 'inherit',
        env: {
            ...process.env,
            NODE_PATH: nodeModulesPath
        }
    });

    if (!existsSync(SOURCE_PROTO_PATH)) {
        throw new Error('WAProto.proto was not generated by local proto extractor');
    }

    return readFileSync(SOURCE_PROTO_PATH, 'utf8');
}

function runPbjs(protoPath, outputPath) {
    execFileSync('npx', ['--yes', '-p', 'protobufjs-cli', 'pbjs', '-t', 'static-module', '-w', 'commonjs', '-o', outputPath, protoPath], {
        stdio: 'inherit'
    });
}

/**
 * Extract all top-level proto type names from the bundled index.js by scanning
 * for `proto.TypeName = (function()` patterns.
 */
function extractTopLevelTypes(bundleContent) {
    const types = [];
    const pattern = /proto\.([A-Z][a-zA-Z]+) = \(function\(\)/g;
    let m;
    while ((m = pattern.exec(bundleContent)) !== null) {
        types.push(m[1]);
    }
    return [...new Set(types)];
}

/**
 * Extract the proto definition for a top-level type from the bundled index.js
 * by parsing the decode function's switch/case to determine field IDs and types.
 */
function extractProtoDefinition(bundleContent, typeName, waVersion) {
    // Extract @interface properties from JSDoc
    const ifacePattern = new RegExp(
        `@interface I${typeName}[\\s\\S]{0,3000}?\\*\\/`,
        'm'
    );
    const ifaceMatch = bundleContent.match(ifacePattern);

    // Check if this is an enum (has valuesById pattern but no @interface)
    const isEnum = !ifaceMatch;
    const enumBodyStart = bundleContent.indexOf(`proto.${typeName} = (function() {\n        var valuesById`);
    if (isEnum && enumBodyStart !== -1) {
        // Extract enum values from jsdoc
        const enumDocPattern = new RegExp(
            `@name proto\\.${typeName}\\s*\\n[\\s\\S]{0,2000}?\\*\\/`,
            'm'
        );
        const enumDoc = bundleContent.match(enumDocPattern);
        if (!enumDoc) return null;

        const lines = [
            `syntax = "proto3";`,
            `package proto;`,
            ``,
            `/// WhatsApp Version: ${waVersion}`,
            ``,
            `enum ${typeName} {`,
        ];
        const valPattern = /@property \{number\} (\w+)=(\d+)/g;
        let vm;
        while ((vm = valPattern.exec(enumDoc[0])) !== null) {
            lines.push(`    ${vm[1]} = ${vm[2]};`);
        }
        lines.push(`}`);
        return lines.join('\n') + '\n';
    }

    if (!ifaceMatch) return null;

    // Parse @property lines
    const props = [];
    const propLinePattern = /@property \{([^}]+)\} \[(\w+)\]/g;
    let pm;
    while ((pm = propLinePattern.exec(ifaceMatch[0])) !== null) {
        props.push({ jsType: pm[1], name: pm[2] });
    }

    // Extract decode block for field IDs
    const decodeMarker = `${typeName}.decode = function decode(reader, length`;
    const decodeStart = bundleContent.indexOf(decodeMarker);
    let fieldIds = {};
    let fieldTypes = {};
    let repeatedFields = new Set();

    if (decodeStart !== -1) {
        const decodeEnd = bundleContent.indexOf('return message;\n        };', decodeStart);
        const decodeBlock = bundleContent.substring(decodeStart, decodeEnd + 100);

        // Match: case X: {\n       message.fieldName = reader.xxx() or .decode(...)
        const caseBlockPattern = /case (\d+):\s*\{([^}]+)\}/gm;
        let cm;
        while ((cm = caseBlockPattern.exec(decodeBlock)) !== null) {
            const fieldId = parseInt(cm[1]);
            const body = cm[2];
            const fieldMatch = body.match(/message\.(\w+)\s*(?:=|\.push\()/);
            if (fieldMatch) {
                const fieldName = fieldMatch[1];
                fieldIds[fieldName] = fieldId;
                if (body.includes('.push(')) {
                    repeatedFields.add(fieldName);
                }
                // Detect type
                if (body.includes('reader.string()')) fieldTypes[fieldName] = 'string';
                else if (body.includes('reader.int32()')) fieldTypes[fieldName] = 'int32';
                else if (body.includes('reader.int64()')) fieldTypes[fieldName] = 'int64';
                else if (body.includes('reader.uint32()')) fieldTypes[fieldName] = 'uint32';
                else if (body.includes('reader.uint64()')) fieldTypes[fieldName] = 'uint64';
                else if (body.includes('reader.bool()')) fieldTypes[fieldName] = 'bool';
                else if (body.includes('reader.bytes()')) fieldTypes[fieldName] = 'bytes';
                else if (body.includes('reader.float()')) fieldTypes[fieldName] = 'float';
                else if (body.includes('reader.double()')) fieldTypes[fieldName] = 'double';
                else if (body.includes('reader.sint32()')) fieldTypes[fieldName] = 'sint32';
                else if (body.includes('reader.sint64()')) fieldTypes[fieldName] = 'sint64';
                else {
                    const decodeRef = body.match(/\$root\.proto\.([A-Z][a-zA-Z]+)\.decode/);
                    if (decodeRef) fieldTypes[fieldName] = decodeRef[1];
                }
            }
        }
    }

    const lines = [
        `syntax = "proto3";`,
        `package proto;`,
        ``,
        `/// WhatsApp Version: ${waVersion}`,
        ``,
        `message ${typeName} {`,
    ];

    const nestedEnums = new Set();

    for (const prop of props) {
        const id = fieldIds[prop.name];
        if (id === undefined) continue; // Skip unknown fields

        const isRepeated = repeatedFields.has(prop.name) || prop.jsType.startsWith('Array.<');
        const prefix = isRepeated ? 'repeated' : 'optional';

        let protoType = fieldTypes[prop.name];
        if (!protoType) {
            // Fallback from jsType
            if (prop.jsType.includes('string')) protoType = 'string';
            else if (prop.jsType.includes('boolean')) protoType = 'bool';
            else if (prop.jsType.includes('Uint8Array')) protoType = 'bytes';
            else if (prop.jsType.includes('number|Long') || prop.jsType.includes('Long|null')) protoType = 'int64';
            else if (prop.jsType.includes('number')) protoType = 'int32';
            else {
                const innerM = prop.jsType.match(/Array\.<(?:proto\.I)?([A-Za-z]+)>/);
                if (innerM) protoType = innerM[1];
                else {
                    const protoM = prop.jsType.match(/proto\.I([A-Za-z]+)/);
                    if (protoM) protoType = protoM[1];
                    else protoType = 'bytes';
                }
            }
        }

        // Detect nested enum reference
        if (prop.jsType.includes(`${typeName}.`)) {
            const enumM = prop.jsType.match(new RegExp(`${typeName}\\.([A-Z][a-zA-Z]+)`));
            if (enumM) {
                nestedEnums.add(enumM[1]);
                protoType = `${typeName}.${enumM[1]}`;
            }
        }

        lines.push(`    ${prefix} ${protoType} ${prop.name} = ${id};`);
    }

    // Add nested enums
    for (const enumName of nestedEnums) {
        const enumDoc = bundleContent.match(
            new RegExp(`@name proto\\.${typeName}\\.${enumName}[\\s\\S]{0,2000}?\\*\\/`, 'm')
        );
        if (enumDoc) {
            lines.push(`    enum ${enumName} {`);
            const valPattern = /@property \{number\} (\w+)=(\d+)/g;
            let vm;
            while ((vm = valPattern.exec(enumDoc[0])) !== null) {
                lines.push(`        ${vm[1]} = ${vm[2]};`);
            }
            lines.push(`    }`);
        }
    }

    lines.push(`}`);
    return lines.join('\n') + '\n';
}

/**
 * Regenerate all per-module wrapper files in WAProto/ after the bundled index.js
 * has been updated.  For each top-level proto type in index.js:
 *   • Creates the sub-directory if missing
 *   • Writes/updates <Type>.js  (re-export wrapper)
 *   • Writes/updates <Type>.d.ts (lightweight stub)
 *   • Writes/updates <Type>.proto (extracted definition, skip if already identical)
 */
function syncPerModuleFiles(bundleContent, waVersion) {
    if (!waVersion) {
        const waVersionMatch = bundleContent.match(/WhatsApp Version: ([\d.]+)/);
        waVersion = waVersionMatch ? waVersionMatch[1]
            : (existsSync(WA_VERSION_PATH) ? readFileSync(WA_VERSION_PATH, 'utf8').trim() : 'unknown');
    }

    // Persist the version for future use
    writeFileSync(WA_VERSION_PATH, waVersion + '\n', 'utf8');

    const types = extractTopLevelTypes(bundleContent);
    console.log(`[proto-sync] Found ${types.length} top-level proto types in index.js`);
    console.log(`[proto-sync] WhatsApp version: ${waVersion}`);

    let created = 0;
    let updated = 0;

    for (const typeName of types) {
        const typeDir = join(WA_PROTO_DIR, typeName);
        if (!existsSync(typeDir)) {
            mkdirSync(typeDir, { recursive: true });
        }

        const jsPath = join(typeDir, `${typeName}.js`);
        const dtsPath = join(typeDir, `${typeName}.d.ts`);
        const protoPath = join(typeDir, `${typeName}.proto`);

        const jsContent = `"use strict";\n\nconst { proto } = require("../index");\n\nmodule.exports = {\n    ${typeName}: proto.${typeName}\n};\n`;

        let isNew = !existsSync(jsPath);

        writeFileSync(jsPath, jsContent, 'utf8');
        if (!existsSync(dtsPath)) {
            writeFileSync(dtsPath, STUB_DTS, 'utf8');
        }

        // Extract and write proto definition (skip if already present and identical)
        const protoDef = extractProtoDefinition(bundleContent, typeName, waVersion);
        if (protoDef) {
            const existing = existsSync(protoPath) ? readFileSync(protoPath, 'utf8') : '';
            if (existing !== protoDef) {
                writeFileSync(protoPath, protoDef, 'utf8');
                if (isNew) created++; else updated++;
            }
        }
    }

    // Remove directories for types that no longer exist in index.js
    const typeSet = new Set(types);
    const entries = readdirSync(WA_PROTO_DIR, { withFileTypes: true });
    for (const entry of entries) {
        if (!entry.isDirectory()) continue;
        const dirName = entry.name;
        if (!typeSet.has(dirName)) {
            console.log(`[proto-sync] Removing obsolete directory: ${dirName}`);
            rmSync(join(WA_PROTO_DIR, dirName), { recursive: true, force: true });
        }
    }

    console.log(`[proto-sync] Per-module sync complete: ${created} created, ${updated} updated`);
}

/**
 * Run TypeScript declaration generation (yarn build:types) so .d.ts files
 * in lib/ are regenerated after WAProto/index.js changes.
 */
function rebuildTypes() {
    console.log('[build] Regenerating TypeScript declarations (yarn build:types)...');
    execFileSync('yarn', ['build:types'], {
        cwd: ROOT_DIR,
        stdio: 'inherit'
    });
    console.log('[build] Type declarations updated.');
}

async function main() {
    const depsRoot = installExtractorDependencies();
    const nodeModulesPath = join(depsRoot, 'node_modules');

    try {
        const source = generateProtoSourceLocally(nodeModulesPath);
        const normalizedSource = source.replace(/\brequired\s+/g, 'optional ');

        writeFileSync(SOURCE_PROTO_PATH, normalizedSource);

        try {
            runPbjs(SOURCE_PROTO_PATH, BUNDLE_PATH);
        }
        finally {
            if (existsSync(SOURCE_PROTO_PATH)) {
                rmSync(SOURCE_PROTO_PATH);
            }

            const corePath = join(WA_PROTO_DIR, 'core.js');
            if (existsSync(corePath)) {
                rmSync(corePath);
            }
        }

        // After regenerating the bundle, sync all per-module wrapper files.
        // Extract the WA version from the proto source (has the version comment).
        const waVersionMatch = normalizedSource.match(/WhatsApp Version: ([\d.]+)/);
        const waVersion = waVersionMatch ? waVersionMatch[1] : undefined;
        const bundleContent = readFileSync(BUNDLE_PATH, 'utf8');
        syncPerModuleFiles(bundleContent, waVersion);

        // Regenerate TypeScript declarations
        rebuildTypes();
    }
    finally {
        rmSync(depsRoot, { recursive: true, force: true });
    }
}

main().catch(error => {
    console.error(error);
    process.exit(1);
});
