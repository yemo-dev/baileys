const { existsSync, mkdtempSync, readFileSync, rmSync, writeFileSync } = require('fs');
const { join } = require('path');
const { tmpdir } = require('os');
const { execFileSync } = require('child_process');
const { syncPerModuleFiles } = require('./sync-wa-proto-modules');

const ROOT_DIR = join(__dirname, '..');
const SCRIPTS_DIR = join(ROOT_DIR, 'scripts');
const WA_PROTO_DIR = join(ROOT_DIR, 'WAProto');
const SOURCE_PROTO_PATH = join(WA_PROTO_DIR, 'WAProto.proto');
const BUNDLE_PATH = join(WA_PROTO_DIR, 'index.js');
const LOCAL_PROTO_EXTRACT_PATH = join(SCRIPTS_DIR, 'proto-extract.js');

function installExtractorDependencies() {
    const depsRoot = mkdtempSync(join(tmpdir(), 'yebail-proto-deps-'));

    execFileSync('npm', ['install', '--no-save', '--prefix', depsRoot, 'request-promise-native', 'acorn', 'acorn-walk'], {
        cwd: ROOT_DIR,
        stdio: 'inherit'
    });

    return depsRoot;
}

function generateProtoSourceLocally(nodeModulesPath) {
    if (!existsSync(LOCAL_PROTO_EXTRACT_PATH)) {
        throw new Error('Missing scripts/proto-extract.js');
    }

    execFileSync('node', [LOCAL_PROTO_EXTRACT_PATH], {
        cwd: SCRIPTS_DIR,
        stdio: 'inherit',
        env: {
            ...process.env,
            NODE_PATH: nodeModulesPath
        }
    });

    if (!existsSync(SOURCE_PROTO_PATH)) {
        throw new Error('WAProto.proto was not generated by local proto extractor');
    }

    return readFileSync(SOURCE_PROTO_PATH, 'utf8');
}

function runPbjs(protoPath, outputPath) {
    execFileSync('npx', ['--yes', '-p', 'protobufjs-cli', 'pbjs', '-t', 'static-module', '-w', 'commonjs', '-o', outputPath, protoPath], {
        stdio: 'inherit'
    });
}

async function main() {
    const depsRoot = installExtractorDependencies();
    const nodeModulesPath = join(depsRoot, 'node_modules');

    try {
        const source = generateProtoSourceLocally(nodeModulesPath);
        const normalizedSource = source.replace(/\brequired\s+/g, 'optional ');

        writeFileSync(SOURCE_PROTO_PATH, normalizedSource);

        try {
            runPbjs(SOURCE_PROTO_PATH, BUNDLE_PATH);
        }
        finally {
            if (existsSync(SOURCE_PROTO_PATH)) {
                rmSync(SOURCE_PROTO_PATH);
            }

            const corePath = join(WA_PROTO_DIR, 'core.js');
            if (existsSync(corePath)) {
                rmSync(corePath);
            }
        }

        // After regenerating the bundle, sync all per-module wrapper files.
        // Extract the WA version from the proto source (has the version comment).
        const waVersionMatch = normalizedSource.match(/WhatsApp Version: ([\d.]+)/);
        const waVersion = waVersionMatch ? waVersionMatch[1] : undefined;
        const bundleContent = readFileSync(BUNDLE_PATH, 'utf8');
        syncPerModuleFiles(bundleContent, waVersion);
    }
    finally {
        rmSync(depsRoot, { recursive: true, force: true });
    }
}

main().catch(error => {
    console.error(error);
    process.exit(1);
});
